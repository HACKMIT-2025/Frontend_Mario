    console.log('üîß Physics Engine Status:')
    console.log('  - Mode: Play Mode')
    console.log('  - Engine Version: Reverted (Stable)')
    console.log('  - Gravity:', gameAPI.getEngine().getPhysicsEngine().getGravity())

