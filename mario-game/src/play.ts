document.addEventListener('keydown', (event) => {
  switch(event.code) {
    case 'KeyP':
      if (gameAPI) {
        gameAPI.pauseGame()
        console.log('üéÆ Game paused/resumed')
      }
      event.preventDefault()
      break
    case 'KeyR':
      if (event.ctrlKey || event.metaKey) {
        event.preventDefault()
        if (confirm('Are you sure you want to restart the game?')) {
          location.reload()
        }
      }
      break
  }
})

