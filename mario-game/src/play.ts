    await gameAPI.startGame()

    console.log('üéÆ Play game started successfully!')

    // Ëá™Âä®ÁîüÊàêÂπ∂‰∏ä‰º†Êà™ÂõæÔºàÂ¶ÇÊûúÂÖ≥Âç°Ê≤°ÊúâÊà™ÂõæÔºâ
